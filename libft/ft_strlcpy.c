#include "libft.h"

unsigned long	ft_strlcpy(char *dst, const char *src, unsigned long sz)
{
	unsigned long	i;

	i = 0;
	if (!sz)
		return (ft_strlen(src));
	while (i++ < sz - 1)
		*dst++ = *src++;
	dst[i] = '\0';
	return (ft_strlen(src));
}
