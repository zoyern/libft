int	strncmp(const char *s1, const char *s2, unsigned int n)
{
	unsigned int	i;
	
	i = 0;
	if (n == 0)
		return (0);
	while(*s1++ && *s2++ && i++ < n - 1)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
	}
	return (*s1 - *s2);
}